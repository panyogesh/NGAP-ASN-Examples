void decode_ngap_data(NGAP_PDU_t* pdu, const_bstring const raw) {
  asn_dec_rval_t dec_ret;
  dec_ret = aper_decode(NULL, &asn_DEF_NGAP_PDU, (void**)&pdu, bdata(raw),
                        blength(raw), 0, 0);

  //xer_fprint(stdout, &asn_DEF_NGAP_PDU, (void*)&pdu);
  xer_fprint(stdout, &asn_DEF_NGAP_PDU, pdu);
  assert(dec_ret.code == RC_OK);
}


